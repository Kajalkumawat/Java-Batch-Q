public class AutomaticType {
    public static void main(String[] args) {
        byte a = 89;
        int b = a;
        System.out.println(a + " " + b);
        long c = 7855;
        double d = c;
        System.out.println(c + " " + d);

        char e = 'a';
        int f = e;
        System.out.println(e + " " + f);
    }
}
